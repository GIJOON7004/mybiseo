const conn = await mysql.createConnection(DATABASE_URL);

// 카테고리 확인/생성
async function ensureCategory(slug, name, description, sortOrder) {
  const [rows] = await conn.execute("SELECT id FROM blog_categories WHERE slug = ?", [slug]);
  if (rows.length > 0) return rows[0].id;
  const [result] = await conn.execute(
    "INSERT INTO blog_categories (name, slug, description, sort_order) VALUES (?, ?, ?, ?)",
    [name, slug, description, sortOrder]
  );
  return result.insertId;
}

// 포스트 생성 (이미 있으면 스킵)
async function createPost(post) {
  const [rows] = await conn.execute("SELECT id FROM blog_posts WHERE slug = ?", [post.slug]);
  if (rows.length > 0) {
    // Update existing post with tags and reading_time
    await conn.execute(
      "UPDATE blog_posts SET tags = ?, reading_time = ?, published = 'published' WHERE slug = ?",
      [post.tags || null, post.readingTime || 5, post.slug]
    );
    console.log(`  🔄 업데이트: ${post.slug}`);
    return;
  }
  await conn.execute(
    `INSERT INTO blog_posts (category_id, title, slug, excerpt, content, meta_title, meta_description, tags, reading_time, published)
     VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, 'published')`,
    [post.categoryId, post.title, post.slug, post.excerpt, post.content, post.metaTitle, post.metaDescription, post.tags || null, post.readingTime || 5]
  );
  console.log(`  ✅ 생성: ${post.slug}`);
}

